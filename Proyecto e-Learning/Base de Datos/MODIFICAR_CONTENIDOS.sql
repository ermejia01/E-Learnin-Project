CREATE PROCEDURE [dbo].[MODIFICAR_CONTENIDO]
(
	@ID_CONTENIDO	INT,
	@DESCRIPCION	VARCHAR(100),
	@TIPO			VARCHAR(30),
	@ID_CATEGORIA	INT,
	@ID_CURSO		INT,
	@ID_TEMA		INT,
	@ARCHIVO		VARBINARY(MAX)
)
AS 
BEGIN
	UPDATE 
		CONTENIDOS
	SET
		DESCRIPCION		= @DESCRIPCION,
		TIPO			= @TIPO,
		ID_CATEGORIA	= @ID_CATEGORIA,
		ID_CURSO		= @ID_CURSO,
		ID_TEMA			= @ID_TEMA,
		ARCHIVO			= @ARCHIVO
	WHERE
		ID_CONTENIDO = @ID_CONTENIDO
END
GO


